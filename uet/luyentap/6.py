#| Cú pháp     | Ý nghĩa                  |
#| ----------- | ------------------------ |
#| `list[i]`   | Lấy phần tử tại vị trí i |
#| `list[-1]`  | Lấy phần tử cuối         |
#| `list[:n]`  | Lấy từ đầu đến n−1       |
#| `list[n:]`  | Lấy từ n đến hết         |
#| `list[a:b]` | Lấy từ a đến b−1         |
